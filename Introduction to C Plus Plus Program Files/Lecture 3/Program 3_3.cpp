// Program 3_3
// Using ! (not) to reverse Boolean values
#include <iostream>
using namespace std;

int main() {
	bool raining = true;
	bool windy = false;
	cout << "raining is: " << raining << endl;
	cout << "windy is: " << windy << endl;
	cout << "Not raining is: " << !raining << endl;                          
	cout << "Not windy is: " << !windy << endl;
}