// Integer Division
#include <iostream>
using namespace std;

int main()
{
	int a, b;
	a = 7;                                                     
	b = 3;
	cout << a + b << endl;  // Addition
	cout << a - b << endl;  // Subtraction
	cout << a * b << endl;  // Multiplication
	cout << a / b << endl;  // Division of INTEGERS (no remainder)
}